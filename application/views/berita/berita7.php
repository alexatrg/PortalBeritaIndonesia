			<h1>Bentrok TNI-Polri Tewaskan 3 Polisi di Papua, Danrem 172/PWY: Saya Bertanggung Jawab</h1>
			SUMBER : kompas.com<p style="color: #b2b2b2">Tanggal: 13/04/2020</p>
			<img src="<?php echo SITE_URL . "/assets/img/image7.jpeg" ?>" style="width: 640px; height: 360;"><br><br>
			<p style="font-family: arial;">KOMPAS.com - Komandan Korem (Danrem) 172/PWY Kolonel Inf J Binsar Parluhutan Sianipar menyesalkan adanya bentrokan yang terjadi antara anggota TNI dengan Polri di Jalan Pemda I, Kampung Kasonaweja, Distrik Mamberamo Tengah, Kabupaten Memberamo Raya, Papua, pada Minggu (12/4/2020) sekitar pukul 07.40 WIT.<br><br>

			Akibat peristiwa itu, tiga anggota Polres Mamberamo Raya tewas saat terlibat bentrok dengan anggota Satgas Yonif 755.<br><br>

			"Saya sesalkan kejadian ini, Saya bertanggung jawab karena satgas ini merupakan bagian dari saya. Saya selaku komandan meminta maaf mungkin selaku komandan saya kurang penekanan terhadap anggota, walaupun ini kesalahan anggota saya," kata Binsar, saat dihubungi, Senin (13/4/2020).<br><br>

			Pasca-kejadian itu, ia memastikan anggota Satgas Yonif 755 yang terlibat bentrok dengan anggota Polres Mamberamo Raya akan ditarik ke Jayapura.<br><br>

			"Pos ini saya tarik dan saya kosongkan, dan akan ditindak sesuai hukum yang berlaku dan percayalah karena nanti akan dikawal. Pos ini nantinya akan digantikan dari satuan teritorial dari kodim," ujarnya.<br><br>

			Penarikan anggota pos tersebut, kata Binsar, tidak lain untuk mempercepat proses penyelidikan atas kejadian tersebut.<br><br>

			"Kami terus berkoordinasi dengan pihak Polda. Di mana kemarin (12/4/2020) dan hari ini kami sudah lakukan penyelidikan kepada anggota pos Pamrahwan dan pers Polres Membramo Raya," ungkap Binsar.<br><br>

			Diberitakan sebelumnya, pertikaian antara oknum TNI-Polri terjadi di Kabupaten Memberamo Raya, Papua, Minggu pagi pukul 07.40 WIT.<br><br>

			Pertikaian ini melibatkan oknum anggota Satgas Pamrahwan Yonif 755/20/3-Kostrad dengan anggota Polres Mamberamo Raya, di Jalan Pemda I, Kampung Kasonaweja, Distrik Mamberamo Tengah.
			Kabid Humas Polda Papua Kombes Polisi Ahmad Musthofa Kamal mengatakan, pertikaian itu akibat salah paham.<br><br>

			Akibat pertikaian itu, tiga anggota polisi meninggal dunia, dan dua anggota polisi lainnya terluka terkena tembakan.<br><br>

			“Akibat kesalahpahaman tersebut 3 orang anggota Polri meninggal dunia atas nama Briptu Marcelino Rumaikewi, Briptu Alexander Ndun dan Bripda Yosias mengalami luka tembak di leher dan dada sebelah kiri dan paha bagian kiri,” kata Kabid Humas Polda Papua, Kombes AM Kamal dikutip dari TribunPapua.com.<br><br>

			Selain itu, ada juga dua anggota polisi yang mengalami luka tembak yakni, Bripka Alva Titaley anggota Reskrim Polsek Mamteng, dan Brigpol Robert Marien anggota SPKT Polsek Mamteng.<br><br>

