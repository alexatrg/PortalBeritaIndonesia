			<p style="font-family: arial;">KOMPAS.com - Pemakaman jenazah MI (62), pasien positif Covid-19 ditolak warga Kota Pasuruan, Jawa Timur, Jumat (10/4/2020).<br><br>

			Pelaksana Tugas Wali Kota Pasuruan Raharto Teno Prasetyo mengatakan, penolakan itu sampai membuat jenazah harus dimakamkan pukul 01.00 WIB, Sabtu (11/2/2020).<br><br>

			Teno menjelaskan, awalnya ada lima tempat yang rencananya dijadikan lokasi pemakaman.<br><br>

			Namun, kelima tempat itu tak bisa digunakan karena kontur tanah yang lebih rendah dari air laut.
			Akhirnya, Pemkot Pasuruan memilih TPU Gadingrejo sebagai TPU terbesar di Kota Pasuruan.<br><br>

			Saat itu lah pemakaman menemui sejumlah kendala.<br><br>

			Di tengah rangkaian pemakaman, sejumlah warga datang berunjuk rasa. Bahkan, ada yang membawa parang.<br><br>

			Namun, setelah diberi pengertian, warga akhirnya menerima.<br><br>

			Pemakaman baru benar-benar usai pada pukul 01.00, Sabtu.<br><br>

			”Warga yang terprovokasi datang beramai-ramai. Bahkan, ada yang membawa parang. Tapi, setelah kami ajak dialog, saya sentuh nuraninya, bahkan saya mencium kening para penggali makam untuk meyakinkan warga, mereka akhirnya mengerti dan bubar,” kata Teno dikutip dari Kompas.id, Senin (13/4/2020).<br><br>

			Teno mengatakan, salah satu pemicu penolakan adalah karena ketakutan warga yang juga disulut oleh aksi provokasi oknum tertentu.<br><br>

			”Warga yang takut dan tidak paham kemudian diprovokasi oleh provokator. Oknum yang hanya ingin mencari panggung. Ia menggerakkan anak-anak muda, yang dengan mudah digerakkan. Namun, setelah diberi penjelasan dan saya yakinkan, mereka akhirnya mengerti dan menerima pemakaman jenazah tersebut,” kata Teno.<br><br>

			Sebelumnya, penolakan pemakaman jenazah pasien Covid-19 juga terjadi di Ungaran, Jawa Tengah.
			Jenazah yang ditolak merupakan seorang perawat.<br><br>

			Penolakan itu berujung penangkapan tiga provokator.<br>

