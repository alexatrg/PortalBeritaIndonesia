			<p style="font-family: arial;">Aprillyas, driver ojol wanita asal Bantul, Yogyakarta, tak menyangka mendapat durian runtuh. Bukan pesanan durian dari pelanggan. Melainkan ditransfer uang Rp 1 juta untuk mengganti pesanan seharga Rp 51 ribu.<br><br>

			Katanya, uang tersebut sebagai pengganti karena pelanggan ketiduran saat April mengantar pesanan ke lokasi. Hingga kemudian, ia memberikan pesanan tersebut kepada orang lain di kompleks area rumah pelanggan.<br><br>

			"Itu order Kamis (7/4) pukul 13.12 WIB, saya dapat orderan atas nama Rachel. Awalnya costumer mau balas chat di aplikasi," ujarnya kepadaÂ kumparan, Senin (13/4).<br><br>

			Akan tetapi, pelanggan tidak membalas pesan dari April saat akan mengantarkan pesanan. Ia hanya bisa menunggu di luar pos penjagaan di area perumahan pelanggan. Sebab, perumahan tersebut membatasi pengunjung dari luar kompleks.<br><br>

			"Ditemenin mas-mas yang jaga, dan aku sudah berkali-kali hubungin. Rasanya ya jengkel posisi lagi kayak gini masih ada orang seperti ini, " keluhnya.<br><br>

			 Ia pasrah setelah menunggu sekitar 2 jam. Karena takut disuspend, ia memberikan pesanan senilai Rp 51 ribu itu kepada penjaga keamanan di kompleks. Ia juga sudah ikhlas dengan hilangnya uang tersebut.<br><br>

			"Sudah bolak-balik nyari nama tersebut dan itu posisi GPS di kos-kosan kebetulan juga dihubungin bapak kosnya. Juga nggak ada penghuninya bernama Rachel," ujarnya.
			Tiba-tiba saja, sekitar pukul 15.00 WIB, pelanggan tersebut mengirimkan pesan kepada April. Yang membuat kaget, pelanggan tersebut mengirim uang senilai Rp 1 juta untuk mengganti pesanan tersebut.
			Setelah melihat review di aplikasi, pelanggan tersebut ketiduran saat menunggu pesanan.
			"Tapi kok di titik pengantaran tersebut nama [pelanggan] tidak ada. Ya berpikiran positif aja, mungkin pake aplikasi temennya," imbuhnya.<br><br>

			April kemudian mengucapkan terima kasih kepada pelanggan tersebut.
			"[Pelanggan] cuma bilang 'semoga berkah buat mbaknya', setelah itu nggak ada percakapan lagi," tutup ojol wanita itu.<br><br><br>

