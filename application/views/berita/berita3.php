			<b>1. Fettuccine Alfredo</b><br>
			<img src="<?php echo SITE_URL . "/assets/img/image3-1.jpeg" ?>"><br>
			<b>Bahan-bahan :</b><br>
			1 bungkus pasta fettuccine (250 gram) <br>
			1/2 cup whipping cream  <br>
			1/2 cup butter  <br>
			1/2 cup keju parmesan (diparut)  Lada hitam  Daun parsley (dicacah)  <br>
			1/2 sdt garam <br>
			<b>Cara Membuat :</b><br>
			1. Rebus pasta selama 15 menit hingga teksturnya "al dente" (tekstur lunak, namun  masih kenyal ketika digigit). Tambahkan sedikit minyak saat merebus agar tidak lengket.   agar tidak lengket.<br>
			 2. Sambil menunggu fettuccine  matang, siapkan panci kecil dan panaskan butter dan whipping cream dengan api sedang. <br>
			3. Jika sudah meleleh sempurna, masukkan keju parmesan, garam, dan lada hitam. Aduk adonan saus ini hingga tercampur rata.  <br>
			4. Tiriskan fettuccine jika sudah matang.  <br>
			5. Masukkan fettuccine dalam adonan saus dan aduk merata.  <br>
			6. Berikan taburan daun parsley agar tampilannya kian cantik.  <br>
			7. Yummy, fettuccine alfredo siap disantap! <br><br>
			<b>2. Fettuccine Aglio Olio</b>
			<img src="<?php echo SITE_URL . "/assets/img/image3-2.jpeg" ?>"><br>
			<b>Bahan-bahan:  </b><br>
			1 bungkus pasta (250 gram)<br>
			10 siung bawang putih (dicincang halus)  <br>
			1 sdt oregano  <br>
			1/4 kg udang (bersihkan dari kepala dan kulitnya)  <br>
			olive oil secukupnya  <br>
			Cabai bubuk  Merica secukupnya  <br>
			Garam secukupnya  <br>
			2 sdt air perasan lemon <br><br>

			<b>Cara membuat :  </b> <br>
			1. Rebus pasta selama 15 menit hingga teksturnya "al dente".   <br>
			2. Jika sudah matang, tiriskan dan tambahkan sedikitolive oil agar tidak lengket.  <br>
			3. Tumis bawang putih dengan sedikit minyak hingga harum.  <br>
			4. Tumis udang hingga warnanya kemerahan. Kemudian, masukkan pasta dan tambahkan garam serta merica. Beri pula perasan air jeruk lemon agar aromanya segar dan tidak amis. Aduk-aduk hingga rata.  <br>
			5. Koreksi rasa. Jika sudah pas, tambahkan oregano dan aduk kembali hingga rata.   <br>
			6. Jika kamu pencinta pedas, kamu bisa menambahkan bubuk cabai diatasnya.  <br>
			7. Sajikan di piring saji dan fettuccine aglio olio-mu pun siap disantap!<br>
