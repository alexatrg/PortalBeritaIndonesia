			<b>3. Spaghetti Bolognese</b>
			<img src="<?php echo SITE_URL . "/assets/img/image3-3.jpeg" ?>"><br>
			<b>Bahan-bahan : </b><br>
			Satu bungkus spaghetti (250 gram)  <br>
			250 gram daging cincang  <br>
			2 siung bawang putih (dicincang halus)  <br>
			1 siung bawang bombay (Potong bulat tipis)  <br>
			2 buah tomat (potong dadu)  <br>
			Pasta tomat secukupnya  <br>
			Keju parut secukupnya  <br>
			400 ml kaldu sapi <br><br>

			<b>Cara Membuat : </b><br>
			1. Rebus pasta selama 15 menit dengan sedikit minyak agar tidak lengket.   <br>
			2. Tumis bawang putih dan bawang bombay dengan sedikit minyak hingga berbau harum.  <br>
			3. masukkan daging cincang dan tumis hingga warnanya kecokelatan.<br>
			4. masukkan saus tomat secukupnya dan potongan tomat.  <br>
			5. Siram dengan air kaldu sapi dan tunggu hingga bumbunya mengental.  <br>
			6. Sajikan spaghetti di atas piring saji.  <br>
			7. Tuangkan saus bolognese dan taburkan keju parut diatasnya.   <br>
			8. Spaghetti bolognese lezatmu pun siap disantap!<br><br>

			<b>4. Fettucine Tuna Carbonara</b><br>
			<img src="<?php echo SITE_URL . "/assets/img/image3-4.jpeg" ?>"><br>
			<b>Bahan-bahan : </b><br>
			1 bungkus pasta fettuccine (250 gram)  <br>
			2 siung bawang putih (dicincang halus)]  <br>
			1/2 bawang bombay (iris bulat halus)  <br>
			3 sdm olive oil  <br>
			Ikan tuna (goreng setengah matang lalu disuwir)  <br>
			1 sdt garam  <br>
			Keju parut  <br>
			4 butir kuning telur  <br>
			1/4 cup susu  <br>
			1/2 sdt merica bubuk  <br>
			Oregano secukupnya<br>

			<B>Cara Membuatnya : </B><br>
			1. Rebus pasta selama 15 menit hingga teksturnya "al dente".   <br>
			2. Jika sudah matang, tiriskan dan tambahkan sedikitolive oil agar tidak lengket.  <br>
			3. Sementara itu, kocok kuning telur bersama keju parut dan merica bubuk.   <br>
			4. Jika sudah tercampur rata, masukkan susu.  <br>
			5. Tumis bawang putih dan bawang bombay dengan sedikit minyak hingga berbau harum. Masukkan tuna  yang sudah disuwir.  <br>
			4. Masukkan fettuccine yang sudah matang tadi dan aduk rata.   <br>
			5. Setelah itu, tuangkan adonan saus dan oregano. Aduk hingga tercampur rata.  <br>
			6. Sajikan pasta di piring saji dan tambahkan parutan keju diatasnya.  <br>
			7. Fettucine tuna carbonara-mu siap disantap. Nyam nyam nyam! <br><br>
			<b>5. Spaghetti Spicy Cheese</b><br>
			<img src="<?php echo SITE_URL . "/assets/img/image3-5.jpeg" ?>"><br>
			<b>Bahan - bahan :</b><br>
			1 bungkus spaghetti (20 gram)  <br>
			2 siung bawang putih (dicincang halus)  <br>
			1/4 cup susu  Smoked beef secukupnya  <br>
			2 butir kuning telur  <br>
			Keju parut secukupnya  <br>
			1 sdm maizena  <br>
			1/2 sdm lada bubuk  <br>
			Garam secukupnya  <br>
			daun parsley secukupnya (cincang halus)  <br>
			Spicy cheese secukupnya<br>
			<b>Cara Memasak :</b><br>
			1. Rebus pasta selama 15 menit dengan sedikit minyak agar tidak lengket. Tunggu sampai teksturnya menjadi "al dente"  <br>
			2. Tumis bawang putih dengan sedikit minyak hingga berbau harum.   <br>
			3. Masukkansmoked beef dan tuang susu cair.   <br>
			4. Kemudian, masukkan keju, lada, garam danspicy cheese. Aduk rata.  <br>
			5. Tuang maizena yang telah dilarutkan dengan sedikit air dan masukkan spaghetti. Aduk hingga tercampur sempurna.  <br>
			6. Masukkan kuning telur kocok dan aduk dengan api kecil.  <br>
			7. Tunggu samapi teksturnya menjadi creamy dan hidangkan di piring saji.  <br>
			8. Agar kian cantik, taburkan parutan keju di atasnya.
