			<h1>LAPAN Bagikan Video Letusan Gunung Anak Krakatau dari Luar Angkasa</h1>
			SUMBER : suara.com<p style="color: #b2b2b2">Tanggal: 13/04/2020</p>
			<img src="<?php echo SITE_URL . "/assets/img/image10-1.jpeg" ?>" style="width: 640px; height: 360;"><br><br>
			<p style="font-family: arial;">Seperti diketahui, gunung berapi yang berada di tengah Selat Sunda itu mengeluarkan dua letusan pada Jumat (10/4/2020) malam. Letusan pertama terjadi pukul 21.58 WIB dan yang kedua pukul 22.35 WIB dengan tinggi kolom abu kurang lebih 500 meter di atas puncak.
			<br><br>
			Pascafenomena tersebut, Lembaga Penerbangan dan Antariksa (LAPAN) membagikan video detik-detik Gunung Anak Krakatau bererupsi dan mengeluarkan abu vulakaniknya.
			Dalam akun Instagram resmi LAPAN, Peneliti Ahli Utama LAPAN Dony Kushardono menerangkan peristiwa tersebut terekam melalui satelit cuaca Himawari.
			Video berdurasi delapan detik tersebut, terlihat abu vulkanik Gunung Anak Krakatau yang membumbung tinggi dan tersapu ke arah barat.<br><br>

			Perlu diketahui, letusan Gunung Anak Krakatau tersebut bukan merupakan sumber dentuman misterius yang terjadi pada Sabtu dini hari.
			Pasalnya, suara yang terdengar dari wilayah Jakarta hingga ke Provinsi Lampung itu cukup menyita perhatian masyarakat.<br><br>
			"Kita sudah kroscek ke lembaga terkait. Jika mengaitkannya dengan suara letusan Anak Krakatau, itu bukan. Badan Vulkanologi mencatat, letusan gunung terjadi sore hari, sedangkan suara misterius itu kan sekitar pukul 2.00 WIB pagi," tutup Jasyanto, Kabag Humas LAPAN saat dihubungi Suara.com, Minggu (12/4/2020).<br><br>

