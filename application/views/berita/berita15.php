			<p style="font-family: arial;">PEMERINTAH Kota Medan mengoperasikan posko Gugus Tugas Percepatan Penanganan Covid-19 Kota Medan seiring peningkatan kasus penyakit ini di Sumatra Utara.<br><br>

			Kepala Dinas Kesehatan Kota Medan Edwin Effendi mengatakan, posko yang berada di Gedung Dharma Wanita, Jalan Sei Rotan, Kecamatan Medan Petisah, sudah beroperasi mulai akhir pekan kemarin.<br><br>

			"Pada tahap awal ini posko akan dibuka sampai dengan hari Kamis 30 April 2020 mendatang," ujarnya, Senin (13/4).<br><br>

			Posko yang dibuka nonstop ini dapat menjadi tempat masyarakat menyampaikan atau mendapatkan laporan atau informasi terkait dengan Covid-19.<br><br>

			Setiap hari, tempat ini disiagakan oleh 50 petugas yang berasal dari tiap organisasi perangkat daerah (OPD), secara bergantian. Mereka dipimpin dua pejabat Pemkot Medan setara kepala dinas yang akan bertugas selaku Komandan Posko.<br><br>

			Selain sebagai pusat informasi, posko ini juga menjadi tempat Tim Gugus Tugas Covid-19 Kota Medan menggelar rapat. Ruang rapat telah diatur sedemikian rupa sesuai dengan protokol kesehatan, misalnya, antar tempat duduk berjarak 1 meter.<br><br>

			Posko juga memiliki fasilitas Press Room yang digunakan tim gugus tugas untuk menyampaikan perkembangan terkini penanganan Covid-19 di Kota Medan.<br><br>

			Kota Medan merupakan salah satu daerah berstatus zona merah penularan Covid-19 di Sumatra Utara. Sebagian besar kasus pasien positif dan PDP yang kini masih terus meningkat, muncul dari Medan.<br><br>

			Karena itu, berbagai kebijakan khusus telah dikeluarkan Pemkot Medan, termasuk pendirian posko tersebut. Selain itu, tambah Edwin, Pemkot Medan juga sudah mewajibkan warganya mengenakan masker saat keluar rumah. Bukan lagi bersifat anjuran atau himbauan. (OL-13)<br><br>

