			<h1>Penyemprotan Cairan Disinfektan ke Tubuh Tak Direkomendasikan untuk Cegah Virus Corona</h1>
			SUMBER : kompas.com<p style="color: #b2b2b2">Tanggal: 30/03/2020</p>
			<img src="<?php echo SITE_URL . "/assets/img/image19.jpeg" ?>" style="width: 640px; height: 360;"><br><br>
			<p style="font-family: arial;">Ketua Tim Pakar Gugus Tugas Penanganan Covid-19 Wiku Adisasmito mengingatkan bahwa cairan disinfektan tidak boleh disemprotkan ke tubuh manusia.<br><br>

			Menurut Wiku, ada cara lain yang dapat dilakukan jika ingin melakukan sterilisasi diri setelah beraktivitas di luar, tanpa harus menyemprotkan disinfektan ke tubuh.<br><br>

			"Metode pencegahan tersebut dapat diganti dengan selalu mencuci tangan, hindari menyentuh area wajah dan langsung segera mandi ketika sampai di rumah," kata Wiku dalam konferensi persnya di Graha BNPB, Jakarta, Senin (30/3/2020).<Br><br>

			Setelah itu, baju yang digunakan saat keluar rumah bisa dicuci dengan sabun dan disetrika sambil disemprotkan cairan disinfektan hipokrolit.<br><br>

			Wiku mengatakan, cairan disinfektan tidak boleh digunakan pada tubuh manusia. Sebab, cairan tersebut bisa membahayakan kulit, mulut dan mata.
			<br><br>

			"Penggunaan dengan UV light dalam konsentrasi yang berlebihan mempunyai potensi jangka panjang menimbulkan kanker kulit," tutur dia.<br><br>

			Selain itu, kata Wiku, cairan disinfektan kurang efektif melindungi manusia dari Covid-19.<br><br>

			Sebab, disinfektan hanya ampuh menghilangkan mikroorganisme yang menempel pada benda-benda mati.<br><br>

			"Sifatnya hanya sementara. Disinfektan ini adalah senyawa kimia yang digunakan di dalam proses dekontaminasi yang membunuh mikroorganisme, virus, bakteri pada objek permukaan benda mati," ucap Wiku.<br><br>

			Diketahui, jumlah pasien positif Covid-19 di Indonesia semakin bertambah.<br><br>

			Juru bicara pemerintah untuk penanganan virus corona Achmad Yurianto menuturkan pemerintah sudah memeriksa lebih dari 6.500 spesimen terkait Covid-19.<br><br>

			Berdasarkan data pemerintah, terdapat 1.285 kasus Covid-19 per Minggu (29/3/2020) siang. Jumlah tersebut bertambah sebanyak 130 kasus dari total 1.155 kasus pada Sabtu (28/3/2020).<br><br>

			"Ada penambahan kasus baru positif sebanyak 130 orang, sehingga jumlah sekarang menjadi 1.285 (kasus)," kata Yurianto saat memberikan keterangan pers di Gedung Badan Nasional Penanggulangan Bencana (BNPB), Minggu.<br><br>

			Dari data itu, terdapat 64 pasien Covid-19 yang dinyatakan sembuh. Terjadi penambahan sebanyak lima pasien yang sembuh dibanding data sebelumnya.<br><br>
