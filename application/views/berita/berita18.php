			<p style="font-family: arial;">KOMPAS.com - Angka infeksi Covid-19 yang semakin tinggi membuat banyak orang semakin khawatir akan keselamatan diri sendiri dan keluarga.<br><br>

			Rasa khawatir memang penting untuk meningkatkan kewaspadaan. Namun jika berlebihan, perasaan itu justru akan menambah beban pikiran dan berujung pada penurunan daya tahan tubuh.<br><br>

			Agar tidak terlalu khawatir, Anda perlu tahu, bahwa virus corona juga punya kelemahan.<br><br>
			Kelemahan ini bisa dimanfaatkan untuk mencegah penularan, sehingga risiko tertular pun bisa lebih rendah. Sejauh ini, para ilmuwan, peneliti, dan petugas kesehatan, telah menyusun cara mencegah penularan Covid-19 melalui kelamahan-kelemahan tersebut.<br><br>

			<b>Apa kelemahan virus corona?</b><br>

			Satu hal yang membuat banyak orang khawatir soal Covid-19 adalah karena belum adanya obat maupun vaksin yang memang dikhususkan untuk mengatasi dan mencegah penyakit ini.<br><br>

			Oleh karenanya, cara terbaik yang bisa dilakukan untuk melindungi diri kita adalah dengan melakukan pencegahan semaksimal mungkin.<br><br>

			Namun, meski menyebar dengan cepat, virus corona juga masih punya kelemahan yang bisa dimanfaatkan untuk memutus rantai penularan. Lalu sebenarnya, apa saja kelemahan virus corona?<br><br>

			<b>1. Mudah hilang dengan pelarut lemak</b><br>
			Apa itu pelarut lemak? Ya betul, pelarut lemak adalah sabun yang sehari-hari kita gunakan. Virus corona, bisa hancur dan mati jika terkena sabun.<br><br>

			Itulah sebabnya, kita dianjurkan untuk rajin cuci tangan dengan air dan sabun untuk mencegah infeksi Covid-19.<br><br>

			Lalu, mengapa sabun efektif untuk membunuh virus corona? Jawabannya ada pada susunan virus itu sendiri. Virus corona pada intinya tersusun atas tiga bagian, yaitu:<br><br>

			- DNA atau RNA yang menjadi inti dari virus<br>
			- Protein yang merupakan bahan baku virus untuk memperbanyak diri<br>
			- Lapisan lemak sebagai pelindung luarnya<br><br>

			Ketiga bagian tersebut sebenarnya tidak terikat dengan kuat satu sama lain. Sehingga, saat lapisan lemak tersebut hancur karena sabun, maka virus tersebut pun akan hancur dan mati.<br><br>

			Jadi, imbauan untuk mencuci tangan adalah langkah yang valid dan sangat efektif untuk mencegah penularan Covid-19.<br><br>

			Jika Anda rajin cuci tangan dengan sabun dan air mengalir, maka kemungkinan virus berpindah dari tangan dan masuk ke dalam tubuh akan berkurang drastis.<br><br>

			<b>2. Bisa dikalahkan oleh antibodi</b><br>

			Infeksi Covid-19 bisa terjadi dalam beberapa tingkat keparahan, mulai dari yang ringan hingga parah.<br><br>

			Pada pasien Covid-19 yang memiliki gejala ringan, infeksi ini bisa sembuh dengan sendirinya selama daya tahan tubuhnya baik.<br><br>

			Sebuah penelitian yang dilakukan di Australia mengamini bahwa salah satu kelemahan virus corona adalah dalam menghadapi antibodi yang sehat.<br><br>

			Penelitian ini melihat secara teratur kadar antibodi yang dihasilkan oleh seorang pasien Covid-19 berusia 47 tahun dengan gejala ringan hingga sedang.<br><br>

			Pasien tersebut tidak memiliki penyakit penyerta seperti hipertensi atau diabetes. Kondisi tubuhnya secara keseluruhan sehat dan hanya terdapat satu infeksi yang sedang terjadi, yaitu Covid-19.<br><br>

			Pada hari ke 7-9 sejak gejala Covid-19 pertama kali muncul pada pasien tersebut, sejumlah antibodi mulai terbentuk di tubuh.<br><br>

			Ini tandanya, tubuh tengah mengeluarkan berbagai senjatanya untuk berusaha melawan virus corona. Beberapa hari setelah antibodi terbentuk, tubuh pasien tersebut mulai membaik.<br><br>

			Memang masih dibutuhkan penelitian lebih lanjut dalam skala yang lebih besar lagi untuk melihat pola “peperangan” antara virus corona dan antibodi.<br><br>

			Namun, penelitian di atas bisa dijadikan sebagai pengingat pentingnya menjaga daya tahan tubuh dengan menjalani gaya hidup yang sehat.<br><br>

			<b>3. Bisa dibunuh dengan disinfektan</b><br>

			Virus corona ada banyak jenisnya. Ada virus corona yang menyebabkan SARS, MERS, dan saat ini jenis yang baru ditemukan, mengakibatkan Covid-19.<br><br>

			Masing-masingnya memang memiliki perbedaan dan masih butuh lebih banyak penelitian. Namun sejauh ini, diketahui bahwa secara umum karakter keluarga coronavirus cukup mirip, yaitu dianggap lemah jika harus berhadapan dengan bahan disinfektan.<br><br>

			Berdasarkan hasil penelitian, virus corona penyebab SARS dan MERS bisa bertahan di permukaan benda seperti metal, kaca, atau plastik hingga beberapa hari.<br><br>

			Meski sejauh ini belum ada penelitian mengenai ketahanan virus penyebab Covid-19 di permukaan, tapi diduga hasilnya tidak jauh berbeda dari sepupu sesama coronavirus lainnya.<br><br>

			Kabar baiknya, virus tersebut dianggap bisa nonaktif dengan bahan disinfektan seperti alkohol dengan kadar 60-70%, hidrogen peroksida 0,5%, atau sodium hipoklorit 0,1% dalam waktu 1 menit.
			Jadi rajin-rajinlah membersihkan permukaan benda yang sering disentuh seperti telepon genggam, gagang pintu, dan meja kerja menggunkaan bahan disinfektan.<br><br>

			<b>4. Melemah di suhu panas</b><br>

			Sejauh ini belum ada penelitian yang menyebut bahwa virus penyebab Covid-19 lemah terhadap panas. Namun, coronavirus penyebab penyakit SARS, terbukti bisa melemah pada suhu panas.
			Berdasarkan data yang diterbitkan oleh badan kesehatan dunia, World Health Organization (WHO), virus penyebab SARS bisa terbunuh pada suhu 56°C.<br><br>

			<b>5. Tidak bisa bertahan lama dipermukaan</b><br>

			Virus corona memang bisa bertahan beberapa hari di permukaan. Namun, seiring berjalannya waktu, virus ini tidak lagi cukup kuat untuk bisa menimbulkan infeksi.<br><br>
			
