			<p style="font-family: arial;">SUMUT, KOMPAS.com - JH (39), warga Desa Sitolu Bahal, Kecamatan Lintong Nihuta, Kabupaten Humbang Hasundutan (Humbahas), Sumatera Utara, tewas disambar petir di Dusun Sabalangit Desa Sitio II, Minggu (12/4/2020) pukul 14.00 WIB.<br><br>

			Saat kejadian, kondisi cuaca hujan, korban bersama suami dan anaknya baru memetik buah alpukat di kebun dan berteduh di bawahnya.<br><br>

			"Informasi yang kami terima, petir tiba-tiba menyambar lalu mengenai tubuh korban. Dan korban meninggal dunia," ujar Paur Subbag Humas Polres Humbahas Bripka Syawal lewat pesan singkatnya kepada Kompas.com, Minggu (12/4/2020).<br><br>
			Syawal menceritakan, sebelum kejadian, korban bersamasuaminya, MTS (40), dan anaknya RS (15) pergi dari dari rumah mereka di Dusun Lumban Situmorang, Desa Sitolu Bahal, Kecamatan Lintong Nihuta, Humbahas menuju Dusun Sabalangit Desa Sitio II untuk mengambil alpukat.<br><br>

			Saat memetik alpukat, hujan tiba-tiba turun, korban bersama keluarganya menghentikan kegiatan mereka dan berteduh di bawah pohon sambil menunggu hujan reda.<br><br>

			Kemudian, tanpa disangka, petir tiba-tiba menyambar korban disertai suara guntur yang menggelegar. Korban meninggal di tempat, sedangkan suami dan anaknya selamat.<br><br>

			"Suami dan anak korban sempat terlempar beberapa meter, namun selamat. Sedangkan korban meninggal di lokasi kejadian, serta mengalami luka bakar di bagian tubuhnya," ujar Syawal.<br><br>

			Syawal mengatakan, jenazah korban sudah dibawa ke RSUD Dolok Sanggul, Humbahas untuk dilakukan visum dan kemudian dikembalikan kepada keluarga.<br><br>


