			<p style="font-family: arial;">Presiden Joko Widodo (Jokowi) menyerukan agar rakyat Indonesia tidak mengucilkan jika ada pasien yang positif virus Corona (COVID-19) di wilayah tempat tinggal.<br><br>

			Jokow mengatakan, jika ada pasien yang positif, seharusnya masyarakat setempat saling tolong menolong.<br><br>

			"Kalau ada pasien positif yang ada di sebuah kampung, betul-betul bukan malah dikucilkan tapi kanan kirinya bisa tolong-menolong," ujar Jokowi dalam rapat terbatas melalui video conference di Istana Merdeka, Jakarta, Senin (13/4/2020).<br><br>

			Untuk melawan viru Corona ini, Jokowi mengajak masyarakat untuk saling gotong royong dan membantu sesama.<br><br>

			"Tolong diingatkan bahwa gotong royong, partisipasi, saling membantu itu bisa ditumbuhkan dari bawah. Ini penting sekali," ucap dia.<br><br>

			Mantan Wali Kota Solo itu memuji gotong royong dan kerukunan antar tetangga yang terjadi di Cimahi, Bandung dalam menghadapi pandemi Corona. "Saya sangat senang sekali kemarin melihat misalnya di Cimahi, kerukunan antartetangga sangat baik. Jadi yang positif diisolasi, tapi tetangganya membantu. hal-hal seperti ini, kegotongroyongan seperti ini yang harus kita terus gaungkan sehingga benar-benar kalau ada isolasi mandiri," katanya..<br><br>

			Lebih lanjut, Jokowi juga mengingatkan hal -hal teknis terkait penggunaan masker, jaga jarak ketika melakukan isolasi.  Jokowi juga tak bosan-bosan menyarankan pentingnya jaga jarak dan isolasi parsial di daerah-daerah, untuk mencegahnya penyebaran Covid-19.<br><br>

			"Mengenai pemakaian masker, kemudian jaga jarak kepada daerah juga isolasi parsial. Saya kira penting sekali harus disampaikan sehingga mereka tahu betul, yang namanya jaga jarak itu apa, yang namanya isolasi parsial itu apa dan ini akan bisa mencegah tersebarnya corona ini lebih meluas," katanya.<br><br>
