			<p style="font-family: arial;">Gubernur Bangka Belitung, Erzaldi Rosman membenarkan jika dirinya telah mengeluarkan surat peringatan terhadap Kepala Dinas Sosial (Kadinsos) Babel terkait surat yang disampaikan Kadinsos Babel ke seluruh Kadinsos Pemkab dan Kota Se Babel untuk mendata serta mengusulkan daftar nama calon penerima bantuan dengan sejumlah syarat, salah satunya beragama islam.<br><br>
			Seharusnya, dikatakan Erzaldi jika surat yang dikirimkan tersebut bukan ditujukan ke dinas sosial Kabupaten/kota, melainkan ke Dewan Masjid Indonesia (DMI). Karena pihaknya, sudah mengajukan bantuan sosial kepada Badan Amil Zakat Nasional (Baznas) wilayah setempat untuk membantu masyarakat yang terdampak COVID-19.<br><br>
			"Karena yang dipergunakan itu uang zakat, kalau uang zakat itukan memang untuk orang Muslim. Harusnya ke DMI, jangan ke dinsos," ujar Erzaldi, Sabtu malam.<br><br>
			Erzaldi pun menyayangkan terkait surat yang di keluarkan Kadinsos Babel. Sehingga membuat kebingungan di kalangan masyarakat Bangka Belitung.<br><br>
			"Keteledoran beliau, membuat surat yang mungkin salah arah. Ini bisa SARA lo orang," katanya.
			Orang nomor satu di Babel ini pun menegaskan jika bantuan sosial yang bukan berasal dari zakat maka semua berhak menerima.<br><br>
			"Kita tidak seperti itu, bantuan sosial ini berlaku untuk semua orang," tegasnya.
			Ia pun menyerahkan proses sanksi yang akan diterima Kadinsos Babel kepada Inspektorat.
			"Kemungkinan akan dinonjobkan," tukasnya.<br><br>

